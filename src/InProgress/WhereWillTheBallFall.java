    public static int[] findBall(int[][] grid) {
        //we check the sum of two consecutive blocks in each case
        //if sum == 0 for that index answer[i] =-1
        //if sum == 2 then i+=1
        //if sum == -2 then i-=1
        // if sumTotal>=0 && sumTotal answer[i]==1
        // if 00=-1 answer[i]=-1
        // if answer[i-1]=-1, then you check back and forth
        int i=0;
        int finalIndex=grid[0].length;
        int[] answers=new int[grid[0].length];
        if(grid[0][0]==-1){
            answers[0]=-1;
            i+=1;
        }
        if(grid[0][grid[0].length-1]==1){
            answers[answers.length-1]=-1;
            finalIndex-=1;
        }

        for(;i<finalIndex;i++){
            int I_temp=i;
            for(int j=0;j<grid.length;j++){
                int SubSum=0;
                if(I_temp>0){
                    if(grid[j][I_temp]==1){
                        SubSum=grid[j][I_temp]+grid[j][I_temp+1];
                    }else{
                        SubSum=grid[j][I_temp]+grid[j][I_temp-1];
                    }
                }else{
                    if(I_temp==grid[0].length-1){
                        I_temp--;
                        break;
                    }else if(I_temp<0){
                        I_temp++;
                        break;
                    }
                    SubSum=grid[j][I_temp]+grid[j][I_temp+1];
                }
                if(SubSum==0){
                    answers[i]=-1;
                    break;
                }else if(SubSum==2){
                    I_temp++;
                }else{
                    I_temp--;
                }
            }
            if(answers[i]!=-1) answers[i]=I_temp;
            //System.out.println(Arrays.toString(answers));
        }
        return answers;
    }
